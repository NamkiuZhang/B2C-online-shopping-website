package com.group6.b2c.viewbean;

import java.util.ArrayList;
import java.util.List;

public class IndexVo {
	List<ProductTypeVo> firstTypes;
	List<RecommendVo> recommends;
	List<ProductVo> hots;
	
	public IndexVo(){
		firstTypes = new ArrayList<ProductTypeVo>();
		recommends = new ArrayList<RecommendVo>();
		hots = new ArrayList<ProductVo>();
	}

	public List<ProductTypeVo> getFirstTypes() {
		return firstTypes;
	}

	public void setFirstTypes(List<ProductTypeVo> firstTypes) {
		this.firstTypes = firstTypes;
	}

	public List<RecommendVo> getRecommends() {
		return recommends;
	}

	public void setRecommends(List<RecommendVo> recommends) {
		this.recommends = recommends;
	}

	public List<ProductVo> getHots() {
		return hots;
	}

	public void setHots(List<ProductVo> hots) {
		this.hots = hots;
	}
	
	
}
