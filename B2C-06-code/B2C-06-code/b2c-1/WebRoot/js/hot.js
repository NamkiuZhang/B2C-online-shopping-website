  function over_rd1(){
  	var selected = document.getElementById("rd_img1");
  	selected.style.cursor = "pointer";
  	if(selected.src.lastIndexOf("imgs/current.png") != -1){
  	}else{
  		var parentpath = selected.src.substring(0, selected.src.length-19);
  		selected.src = parentpath+"imgs/current.png";
  		document.getElementById("rd_img2").src =parentpath+ "imgs/notcurrent.png";
  		document.getElementById("rd_img3").src =parentpath+ "imgs/notcurrent.png";
  		document.getElementById("rd_img4").src =parentpath+ "imgs/notcurrent.png";
  		document.getElementById("rd_img5").src =parentpath+ "imgs/notcurrent.png";
  		
  		document.getElementById("rd1").style.zIndex=2;
  		document.getElementById("rd2").style.zIndex=1;
  		document.getElementById("rd3").style.zIndex=1;
  		document.getElementById("rd4").style.zIndex=1;
  		document.getElementById("rd5").style.zIndex=1;
  	}
  }
  
