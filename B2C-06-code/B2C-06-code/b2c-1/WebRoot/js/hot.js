  function click_left(){
  	for(var i=1;i<6;i++){
  		if(document.getElementById("rd_img"+i).src.lastIndexOf("imgs/current.png") != -1){
  			switch(i){
  				case 1:
  					over_rd5();
  				break;
  				case 2:
  					over_rd1();
  				break;
  				case 3:
  					over_rd2();
  				break;
  				case 4:
  					over_rd3();
  				break;
  				case 5:
  					over_rd4();
  				break;
  			}
  			break;
  		}
  	}
  }
  
